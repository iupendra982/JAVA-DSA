package Bit_Manipulation;

public class BitManipulation {
    public static void main(String[] args) {
        System.out.println(5&6);//binary and 
        System.out.println(5|6);//binary or
        System.out.println(5^6);//binary xor
        System.out.println(~5);//one's complement it is implementing MSB and LSB concept for 
        //negative number using 2's Complement 
        System.out.println(5<<2);//binary left shift operator
        //Formulae  (a<<b) = a * 2^b  
        System.out.println(6>>1);//binary right shift operator
        //Formulae  (a>>b) = a / 2^b
        
    }
}
